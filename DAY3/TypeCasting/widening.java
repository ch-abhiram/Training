package DAY3.TypeCasting;

public class widening {
    public static void main(String[] args){
        //widening
        byte a = 10;
        int b = a;
        System.out.println(b);

        int c = 10;
        double d = c;
        System.out.println(d);

        char ch = 'a';
        int e = ch;
        System.out.println(e);

        byte by = 32;
        float f = by;
        System.out.println(f);

    }
}
