package DAY19.Wrapper;

public class boxing {
    public static void main(String[] args) {
        int a = 10;
        //autoboxing
        Integer b = a;
        System.out.println(b);
        byte by = 1;
        Byte By = by;

        Short s = 2;

        Long l = 1234566l;
        Float f = 23.4f;
        Double D = 123214.34;
        Character ch = 'a';
        Boolean B = true;
    }
}
